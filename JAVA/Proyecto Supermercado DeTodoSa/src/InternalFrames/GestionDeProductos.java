    private void jbEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarProductoActionPerformed
        Iterator<Producto> it = MenuPrincipal.listaProductos.iterator();

        try {
            int codigo = Integer.parseInt(jtCodigo.getText());
            while (it.hasNext()) {
                if (it.next().getCodigo() == codigo) {
                    it.remove();
                }
            }
            JOptionPane.showMessageDialog(this, "El Producto se ha eliminado exitosamente");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el código de Producto");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        }

    }//GEN-LAST:event_jbEliminarProductoActionPerformed


