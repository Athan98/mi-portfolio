    private void jbNuevoProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNuevoProductoActionPerformed
        try {
            int cod = Integer.parseInt(jtCodigo.getText());
            String desc = jtDescripcion.getText();
            Double precio = Double.parseDouble(jtPrecio.getText());
            Categorias cat = (Categorias) jcCategorias.getSelectedItem();
            int stock = Integer.parseInt(jtStock.getText());
            Producto prod = new Producto(cod, desc, precio, stock, cat);
            MenuPrincipal.listaProductos.add(prod);
            JOptionPane.showMessageDialog(this, "El Producto fue registrado exitosamente");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        }
    }//GEN-LAST:event_jbNuevoProductoActionPerformed

    private void jbBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBusquedaActionPerformed
        try {
            for (Producto prod : MenuPrincipal.listaProductos) {
                if (prod.getCodigo() == (Integer.parseInt(jtCodigo.getText()))) {
                    jtDescripcion.setText(prod.getDescripción());
                    jtPrecio.setText(prod.getPrecio() + "");
                    jtStock.setText(prod.getStock() + "");
                    jcCategorias.setSelectedItem(prod.getRubros());
                }
            }
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el código de Producto");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        }

    }//GEN-LAST:event_jbBusquedaActionPerformed

    private void jbModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarProductoActionPerformed

        try {
            Integer newCodigo = Integer.parseInt(jtCodigo.getText());
            String newDescripcion = jtDescripcion.getText();
            Double newPrecio = Double.parseDouble(jtPrecio.getText());
            Integer newStock = Integer.parseInt(jtStock.getText());
            Categorias newCategoria = (Categorias) jcCategorias.getSelectedItem();
            int respuesta = JOptionPane.showConfirmDialog(this, "¿Realmente desea realizar los cambios?", "ALERTA!!!", JOptionPane.YES_NO_OPTION);
            if (respuesta == JOptionPane.YES_OPTION) {
                for (Producto prod : MenuPrincipal.listaProductos) {
                    if (prod.getCodigo()==newCodigo) {
                        MenuPrincipal.listaProductos.remove(prod);
                        prod.setCodigo(newCodigo);
                        prod.setDescripción(newDescripcion);
                        prod.setPrecio(newPrecio);
                        prod.setRubros(newCategoria);
                        prod.setStock(newStock);
                        MenuPrincipal.listaProductos.add(prod);
                        break;
                    } else if (respuesta == JOptionPane.NO_OPTION) {
                        break;
                    }

                }
            }
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el código del Producto");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
            jtCodigo.setText("");
            jtDescripcion.setText("");
            jtPrecio.setText("");
            jtStock.setText("");
        }

    }//GEN-LAST:event_jbModificarProductoActionPerformed

