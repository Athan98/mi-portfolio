    private void jbAgregarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarTareaActionPerformed
        try {
            if (jtEscribirTarea.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe ingresar una tarea");
                return;
            }
            
            Tareas tareas = new Tareas(jtEscribirTarea.getText(), false);
            cargarDatos(tareas);
            JOptionPane.showMessageDialog(this, "La tarea ha sido agregada exitosamente");
            jtEscribirTarea.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
            jtEscribirTarea.setText("");
        }
        
    }//GEN-LAST:event_jbAgregarTareaActionPerformed

    private void jbEliminarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarTareaActionPerformed
        try {
            int filaSeleccionada = jtTareas.getSelectedRow();
            if (filaSeleccionada != -1) {
                modelo.removeRow(filaSeleccionada);
                JOptionPane.showMessageDialog(this, "La tarea ha sido borrada exitosamente");
            } else {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un elemento de la lista");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
            jtEscribirTarea.setText("");
        }
    }//GEN-LAST:event_jbEliminarTareaActionPerformed

    private void jtTareaCompletadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtTareaCompletadaActionPerformed
        int filaSeleccionada = jtTareas.getSelectedRow();
        Tareas newTarea = new Tareas(jtTareas.getName(), true);
        if (filaSeleccionada != -1) {
            modelo.setValueAt(CompletadaNoCompletada(newTarea), filaSeleccionada, 1);
        }
    }//GEN-LAST:event_jtTareaCompletadaActionPerformed

