    private void armarCabecera() {
        
        modelo.addColumn("TAREA");
        modelo.addColumn("ESTADO");
        jtTareas.setModel(modelo);
    }
    
    private void cargarDatos(Tareas tareas) {
        modelo.addRow(new Object[]{tareas.getNombre(), CompletadaNoCompletada(tareas)});
    }
    
    private String CompletadaNoCompletada(Tareas tareas) {
        if (tareas.isEstado() == false) {
            return "NO COMPLETADA";
        } else {
            return "COMPLETADA";
        }
    }
